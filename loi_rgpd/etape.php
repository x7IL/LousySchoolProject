                        <div class="principe">
                            <div class="row"><div class="col-xs-12 intro"><p style="font-size: 24px; line-height: 30px;">Le 25 mai 2018, le règlement européen est entré en application. De nombreuses formalités auprès de la CNIL disparaîssent. En contrepartie, la responsabilité des organismes est renforcée. Ils doivent désormais assurer une protection optimale des données à chaque instant et être en mesure de la démontrer en documentant leur conformité.</p>

                                    <span class="w3-tag"><p class="name">Etape 1</p></span>
                                    <h3>Désigner un pilote</h3>
                                    <p>Pour piloter la gouvernance des&nbsp;données personnelles de votre structure, vous aurez besoin d'un&nbsp;véritable chef d’orchestre qui exercera une mission d’information, de conseil et de contrôle en interne&nbsp;: le délégué à la protection des données. En attendant 2018, vous pouvez d’ores et déjà désigner un «&nbsp;correspondant informatique et libertés&nbsp;», qui vous donnera un temps d'avance et vous permettra d'organiser les actions à mener.</p>
                                    <p><a href="https://www.cnil.fr/fr/designer-un-pilote">&gt; En savoir plus</a></p>
                                    <hr>

                                    <span class="w3-tag"><p class="name">Etape 2</p></span>
                                    <h3>Cartographier vos traitements de données personnelles</h3>
                                    <p>Pour mesurer concrètement l’impact du règlement européen sur la protection des données&nbsp;que vous traitez, commencez par recenser de façon précise vos traitements de données personnelles. L'élaboration d'un&nbsp;registre des traitements vous permet de faire le point.</p>
                                    <p><a href="https://www.cnil.fr/fr/cartographier-vos-traitements-de-donnees-personnelles">&gt; En savoir plus</a></p>
                                    <hr>

                                        <span class="w3-tag"><p class="name">Etape 3</p></span>
                                    <h3>Prioriser les actions à mener</h3>
                                    <p>Sur la base de votre registre, identifiez&nbsp;les actions à mener pour vous conformer aux obligations actuelles et à venir. Priorisez ces actions au regard des risques que font peser vos traitements sur les droits et les libertés des personnes concernées.&nbsp;</p>
                                    <p><a href="https://www.cnil.fr/fr/prioriser-les-actions-mener">&gt; En savoir plus</a></p>
                                    <hr>

                                        <span class="w3-tag"><p class="name">Etape 4</p></span>
                                    <h3>Gérer les risques</h3><p>Si vous avez identifié des&nbsp;traitements de données personnelles susceptibles d'engendrer des risques élevés pour les droits et libertés des personnes concernées, vous devrez mener, pour chacun de ces traitements, une analyse d'impact relative à la protection des données (AIPD).</p>
                                    <p><a href="https://www.cnil.fr/fr/gerer-les-risques">&gt; En savoir plus</a></p>
                                    <hr>

                                        <span class="w3-tag"><p class="name">Etape 5</p></span>
                                    <h3>Organiser les processus internes</h3>
                                    <p>Pour assurer un haut niveau de protection des données personnelles en permanence, mettez&nbsp;en place des procédures internes qui garantissent la prise en compte de la protection des données à tout moment, en prenant&nbsp;en compte l’ensemble des événements qui peuvent survenir au cours de la vie d’un traitement (ex&nbsp;: faille de sécurité, gestion des demande de rectification ou d’accès, modification des données collectées, changement de prestataire).&nbsp;</p>
                                    <p><a href="https://www.cnil.fr/fr/organiser-les-processus-internes">&gt; En savoir plus</a></p>
                                    <hr>

                                        <span class="w3-tag"><p class="name">Etape 6</p></span>
                                    <h3>Documenter la conformité </h3>
                                    <p>Pour&nbsp;prouver votre conformité au règlement, vous devez constituer et regrouper la documentation nécessaire. Les actions et documents réalisés à chaque étape doivent être réexaminés et actualisés régulièrement pour assurer une protection des données en continu.</p>
                                    <p><a href="https://www.cnil.fr/fr/documenter-la-conformite">&gt; En savoir plus</a></p>
